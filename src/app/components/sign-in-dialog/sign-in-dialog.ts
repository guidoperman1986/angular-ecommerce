import { DialogRef } from '@angular/cdk/dialog';
import { Component, inject, signal } from '@angular/core';
import { NonNullableFormBuilder, ReactiveFormsModule, Validators } from '@angular/forms';
import { MatAnchor, MatIconButton } from "@angular/material/button";
import { MAT_DIALOG_DATA, MatDialog, MatDialogClose } from "@angular/material/dialog";
import { MatFormField, MatPrefix, MatSuffix } from "@angular/material/form-field";
import { MatIcon } from "@angular/material/icon";
import { MatInput } from '@angular/material/input';
import { SignInParams } from '../../models/user';
import { ProductStore } from '../../store/product-store';
import { SignUpDialog } from '../sign-up-dialog/sign-up-dialog';

@Component({
  selector: 'app-sign-in-dialog',
  imports: [
    MatIconButton,
    MatIcon,
    MatDialogClose,
    MatFormField,
    MatInput,
    MatPrefix,
    MatSuffix,
    ReactiveFormsModule,
    MatAnchor
  ],
  template: `
    <div class="p-8 max-w-[400px] flex flex-col bg-white rounded-xl">
      <div class="flex justify-between mb-2">
        <h2 class="text-xl font-medium self-center">Sign In</h2>
        <button matIconButton class="" mat-dialog-close>
          <mat-icon>close</mat-icon>
        </button>
      </div>      
      <p class="text-sm text-gray-500">Sign in to your account to continue shopping</p>

      <form class="mt-3" [formGroup]="signInForm" (ngSubmit)="signIn()">
        <mat-form-field class="w-full mb-4">
          <input type="text" matInput formControlName="email" placeholder="Enter your email">
          <mat-icon matPrefix>email</mat-icon>
        </mat-form-field>
        <mat-form-field class="w-full mb-4">
          <input type="password" matInput formControlName="password" [type]="passwordVisible() ? 'text' : 'password'" placeholder="Enter your password">
          <mat-icon matPrefix>lock</mat-icon>
          <button
            matSuffix
            matIconButton
            type="button"
            class="mr-2"
            (click)="passwordVisible.set(!passwordVisible())"
          >
            <mat-icon [fontIcon]="passwordVisible() ? 'visibility_off' : 'visibility'"></mat-icon>
          </button>
        </mat-form-field>
        <button type="submit" matButton="filled" class="w-full">Sign In</button>
      </form>

      <p class="text-sm text-gray-500 mt-2 text-center">
        Don't have an account?
        <a class="text-blue-600 cursor-pointer" (click)="openSignUpDialog()">Sign Up</a>
      </p>
    </div>

  `,
  styles: ``
})
export class SignInDialog {
  store = inject(ProductStore);
  fb = inject(NonNullableFormBuilder);
  signInForm = this.fb.group({
    email: ['johnd@test.com', Validators.required],
    password: ['test123', Validators.required]
  })
  data = inject<{ checkout: boolean }>(MAT_DIALOG_DATA);
  dialogRef = inject(DialogRef);
  dialog = inject(MatDialog);

  passwordVisible = signal<boolean>(false);

  signIn() {
    if (!this.signInForm.valid) {
      this.signInForm.markAllAsTouched();
      return;
    }

    const { email, password } = this.signInForm.value;

    this.store.signIn({ email, password, checkout: this.data?.checkout, dialogId: this.dialogRef.id } as SignInParams);
  }

  openSignUpDialog() {
    this.dialogRef.close();

    this.dialog.open(SignUpDialog, {
      disableClose: true,
      data: {
        checkout: this.data?.checkout
      }
    })
  }
}
